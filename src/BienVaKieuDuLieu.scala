object BienVaKieuDuLieu {
  def main(args: Array[String]): Unit = {
    val i:Int = 10
    val f:Float = 20.5f
    val d:Double = 20.5
    val b:Boolean = true
    val c:Char = 'a'
    val s:String = "Hà Nội"
    System.out.println("i = " + i)
    System.out.println("f = " + f)
    System.out.println("d = " + d)
    System.out.println("b = " + b)
    System.out.println("c = " + c)
    System.out.println("s = " + s)
  }
}
